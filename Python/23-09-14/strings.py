# Title function for string returns the string with first letter of every word in uppercase and rest in lowercase
s = "hello, World"
print(s.title())

# Count method in strings
print("hello, World, hello, world".count("hello"))

# find funcion for strings
print("hello, world. This world is messed up".find("is"))

# isspace(), isalnum() and islower() function for strings
print("helLo1234".islower())

# lsstrip() and rstrip() for strings which are used to strip spaces from the left or the right and also cummalative strip()

# join() function for strings
print("hello, world".join("09"))

# partition() function for strings
print("Hello, world".partition(chr(32)))

# split() function for strings
print("Hello, world".split(chr(32)))
